package com.icaballero.bucles05;

public class Bucles {

	public static void main(String[] args) {
		
		
		for (int i = 0; i < 10; i++) {
			
			System.out.println("Hola " +i);
			
		}
		
		System.out.println();

		int j=0;
		while (j<10) {
			System.out.println("Hola " + j);
			j++;
		}
		
		System.out.println();
		
		int k =0;
		do {
			System.out.println("Hola " + k);
			k++;
		} while (k<10);
		
		
		
	}

}
