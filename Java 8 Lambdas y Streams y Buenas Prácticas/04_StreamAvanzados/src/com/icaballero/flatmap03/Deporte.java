package com.icaballero.flatmap03;

public class Deporte {
	
	private String nombre;
	private int horas;
	

	public Deporte(String nombre, int horas) {
		super();
		this.nombre = nombre;
		this.horas = horas;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getHoras() {
		return horas;
	}
	public void setHoras(int horas) {
		this.horas = horas;
	}
	
	
	
	

}
