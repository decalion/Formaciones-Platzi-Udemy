package com.icaballero.hashmap;

import java.util.Objects;

/**
 *
 * @author ismael
 */
public class Agenda implements Comparable<Agenda> {

    private String nombre;
    private String telefono;
    private String edad;

    public Agenda(String nombre, String edad, String telefono) {
        this.nombre = nombre;
        this.telefono = telefono;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setATelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }

    @Override
    public String toString() {
        return "Agenda{" + "nombre=" + nombre + ", telefono=" + telefono + ", edad=" + edad + '}';
    }

    @Override
    public int compareTo(Agenda agenda) {
        
        return this.getNombre().charAt(0) < agenda.getNombre().charAt(0) ? 1 : 0;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 17 * hash + Objects.hashCode(this.nombre);
        hash = 17 * hash + Objects.hashCode(this.telefono);
        hash = 17 * hash + Objects.hashCode(this.edad);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Agenda other = (Agenda) obj;
        if (!Objects.equals(this.nombre, other.nombre)) {
            return false;
        }
        if (!Objects.equals(this.telefono, other.telefono)) {
            return false;
        }
        return true;
    }

    
    
    
    
}
